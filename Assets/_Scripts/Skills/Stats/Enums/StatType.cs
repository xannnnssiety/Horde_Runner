public enum StatType
{
    // --- Статы Персонажа ---
    MaxHealth,          // Максимальное здоровье
    Armor,              // Броня (например, для снижения урона)
    MoveSpeed,          // Скорость передвижения
    PickupRadius,       // Радиус подбора опыта/предметов

    // --- Глобальные Статы Умений ---
    Damage,             // % Общего урона
    AreaOfEffect,       // % Области действия (размер)
    Cooldown,           // % Снижения перезарядки
    Duration,           // % Длительности умений (например, ауры или эффекты на врагах)
    Amount,             // + К количеству снарядов/объектов (плоский бонус)

    // --- Статы Снарядов ---
    ProjectileSpeed,    // % Скорости полета снарядов
    RicochetChance,     // Шанс рикошета (например, 5 для 5%)
    RicochetCount,     // Максимальное количество отскоков (плоское значение)
    /* ProjectilePierce,   // + К пробиванию врагов (плоский бонус)*/ // В общей прокачке не используется, возможно нужно будет для прокачки скиллов активных снарядов


    // --- Уникальные/Специальные ---
    Luck,               // Удача (может влиять на шанс крита, выпадение наград)
    ExperienceGain,     // % Получения опыта
    CurrencyGain        // % Получения валюты для мета-прогрессии
}